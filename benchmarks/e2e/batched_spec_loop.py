#!/usr/bin/env python3
"""
Batched, KV-cached, CUDA-graph-captured speculative decode over a mixed batch.

This is the deployment path, and the numbers only make sense here. Two effects have to be
in place at once before a speculative tier can be compared to plain decode honestly:

  KV cache. Without one, a target step recomputes the whole prefix, so a single decoded
  token costs as much as verifying gamma+1 of them and speculation looks free. Turning the
  cache on collapses the target step by more than an order of magnitude, and any draft that
  was hiding behind the recompute is suddenly the bill.

  Graph capture. A 160m draft step run eagerly costs a large fraction of a 7B target step,
  which is not arithmetic. It is Python and kernel launches on a model 44x smaller than
  the one it is trying to save work for. Captured, the draft costs roughly what its
  parameter count says it should.

Cache layout. Every row writes the same gamma+1 slots per iteration, so the write head
advances uniformly and cache_position stays one 1-D tensor: shapes are static and the step
is capturable. Rows still accept different numbers of tokens, so slots past a row's
accepted length are marked invalid in a per-row validity mask and a rejected key is simply
never attended. That is a rollback with no cropping and no reallocation, which is what lets
ragged acceptance live inside a static graph.

Deferred draft write. The draft must end an iteration holding every token the target
committed, which when all gamma are accepted includes p_gamma, generated by the draft but
never fed back to it. Rather than spend a whole extra draft forward every iteration on a
case only some rows are in, the write of p_gamma rides along in the next iteration's first
draft forward as a 2-token chunk over slots [W-1, W]. Rows that did not accept all gamma
leave slot W-1 invalid. A 2-token draft forward costs what a 1-token one costs, so the
draft runs gamma forwards per iteration instead of gamma+1.

Throughput is steady-state decode at a full batch, which is what a continuously batched
server sustains: slots stay occupied, so the clock is not held hostage by whichever single
row drew the worst acceptance. Every token counted is a committed token.
"""
import os
import sys
import time

import torch
from transformers.cache_utils import StaticCache

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from serve_multitenant import STATE, pad_batch  # noqa: E402

NEG = torch.finfo(torch.float16).min


def make_cache(model, B, MAX, device, dtype):
    try:
        return StaticCache(config=model.config, max_batch_size=B, max_cache_len=MAX,
                           device=device, dtype=dtype)
    except TypeError:
        return StaticCache(config=model.config, batch_size=B, max_cache_len=MAX,
                           device=device, dtype=dtype)


def mask4d(valid, W, S, B, MAX, device):
    """[B,1,S,MAX] additive mask: query j (at slot W+j) attends slot s iff s is valid for
    that row and s <= W+j."""
    slots = torch.arange(MAX, device=device).view(1, 1, 1, MAX)
    q = (W + torch.arange(S, device=device)).view(1, 1, S, 1)
    ok = (slots <= q) & valid.view(B, 1, 1, MAX)
    return torch.where(ok, torch.zeros((), dtype=torch.float16, device=device),
                       torch.full((), NEG, dtype=torch.float16, device=device))


class Step:
    """A CUDA-graphed fixed-shape [B, S] step over a StaticCache.

    Every input lives in a static buffer; replay reads whatever was copied in. The LoRA
    mode is a python branch inside the wrapped linear and is therefore baked at capture
    time (a graph captured with lora on always applies lora), while the routing vector
    is a tensor read and stays rewritable between replays.
    """

    def __init__(self, model, cache, B, S, MAX, device, lora=False):
        self.model, self.cache, self.B, self.S, self.MAX = model, cache, B, S, MAX
        self.ids = torch.zeros(B, S, dtype=torch.long, device=device)
        self.pos = torch.zeros(B, S, dtype=torch.long, device=device)
        self.cp = torch.zeros(S, dtype=torch.long, device=device)
        self.mask = torch.zeros(B, 1, S, MAX, dtype=torch.float16, device=device)
        # the wrapped linear flattens x to [B*S, in] before BGMV, so the routing vector
        # carries one adapter id per TOKEN, not per row. A [B]-sized idx silently misroutes
        # every token past the first whenever S > 1, which costs acceptance without ever
        # breaking correctness, because the target still verifies.
        self.lora_idx = (torch.zeros(B * S, dtype=torch.long, device=device)
                         if lora else None)
        self.graph = None
        self.out = None

    def _bind(self):
        if self.lora_idx is None:
            STATE["mode"] = "none"
        else:
            STATE["mode"] = "lora"
            STATE["idx"] = self.lora_idx

    def _fwd(self):
        return self.model(input_ids=self.ids, position_ids=self.pos,
                          attention_mask=self.mask, cache_position=self.cp,
                          past_key_values=self.cache, use_cache=True).logits

    @torch.inference_mode()
    def capture(self, warmup=3):
        self._bind()
        s = torch.cuda.Stream()
        s.wait_stream(torch.cuda.current_stream())
        with torch.cuda.stream(s):
            for _ in range(warmup):          # also JITs the BGMV triton kernels
                self._fwd()
        torch.cuda.current_stream().wait_stream(s)
        torch.cuda.synchronize()
        self.graph = torch.cuda.CUDAGraph()
        with torch.cuda.graph(self.graph):
            self.out = self._fwd()
        return self

    @torch.inference_mode()
    def run(self, ids, pos, cp, mask, idx=None):
        self.ids.copy_(ids)
        self.pos.copy_(pos)
        self.cp.copy_(cp)
        self.mask.copy_(mask)
        if idx is not None and self.lora_idx is not None:
            self.lora_idx.copy_(idx.repeat_interleave(self.S) if self.S > 1 else idx)
        self.graph.replay()
        return self.out


@torch.inference_mode()
def prefill(model, cache, prompts, valid, MAX, pad_id, device, adapter=None):
    """Eager prefill at slots [0, S0). Returns (S0, L, pending)."""
    ids, pm, pos = pad_batch(prompts, pad_id, device)
    B, S0 = ids.shape
    valid.zero_()
    valid[:, :S0] = pm.bool()
    if adapter is None:
        STATE["mode"] = "none"
    else:
        STATE["mode"] = "lora"
        STATE["idx"] = adapter.repeat_interleave(S0)
    out = model(input_ids=ids, position_ids=pos,
                attention_mask=mask4d(valid, 0, S0, B, MAX, device),
                cache_position=torch.arange(S0, device=device),
                past_key_values=cache, use_cache=True)
    STATE["mode"] = "none"
    return S0, pm.sum(1), out.logits[:, -1].argmax(-1)


@torch.inference_mode()
def run_tier(kind, tgt_dec, tgt_ver, drf1, drf2, target, draft, prompts, adapter, gamma,
             iters, valid, MAX, pad_id, device, K, record=None):
    """Steady-state decode with every slot occupied.

    drf1 / drf2 are the graphed 1- and 2-token draft steps; the 2-token one carries the
    deferred p_gamma. tgt_ver has S = gamma+1.
    """
    B = len(prompts)
    S0, L, pend = prefill(target, tgt_dec.cache, prompts, valid, MAX, pad_id, device)
    W = S0 + 1                       # slot S0 is the first deferred-write scratch slot
    if kind != "no_spec":
        dvalid = valid.clone()
        prefill(draft, drf1.cache, prompts, dvalid, MAX, pad_id, device,
                adapter=adapter if kind == "warp" else None)

    ar = torch.arange(B, device=device)
    off = torch.arange(gamma + 1, device=device).view(1, -1)
    slots = torch.arange(MAX, device=device)
    acc_tot = torch.zeros(B, dtype=torch.long, device=device)
    last = torch.zeros(B, dtype=torch.long, device=device)    # previous p_gamma
    fully = torch.zeros(B, dtype=torch.bool, device=device)   # ... and whether it was kept
    toks = [[p.item()] for p in pend] if record else None
    total = 0

    torch.cuda.synchronize()
    t0 = time.time()
    for _ in range(iters):
        if kind == "no_spec":
            valid[:, W] = True
            lg = tgt_dec.run(pend.view(B, 1), L.view(B, 1), slots[W:W + 1],
                             mask4d(valid, W, 1, B, MAX, device))
            pend = lg[:, -1].argmax(-1)
            L, W, total = L + 1, W + 1, total + B
            if record:
                for i, t in enumerate(pend.tolist()):
                    toks[i].append(t)
            continue

        dvalid[:, W - 1] = fully                    # deferred p_gamma, valid where kept
        dvalid[:, W] = True
        lg = drf2.run(torch.stack([last, pend], dim=1), torch.stack([L - 1, L], dim=1),
                      slots[W - 1:W + 1], mask4d(dvalid, W - 1, 2, B, MAX, device),
                      idx=adapter)
        nt = lg[:, -1].argmax(-1)
        props = [nt]
        for j in range(1, gamma):
            dvalid[:, W + j] = True
            lg = drf1.run(nt.view(B, 1), (L + j).view(B, 1), slots[W + j:W + j + 1],
                          mask4d(dvalid, W + j, 1, B, MAX, device), idx=adapter)
            nt = lg[:, -1].argmax(-1)
            props.append(nt)
        prop = torch.stack(props, dim=1)                          # [B, gamma]

        valid[:, W:W + gamma + 1] = True
        lg = tgt_ver.run(torch.cat([pend.view(B, 1), prop], dim=1), L.view(B, 1) + off,
                         slots[W:W + gamma + 1],
                         mask4d(valid, W, gamma + 1, B, MAX, device))
        pred = lg.argmax(-1)

        acc = (pred[:, :gamma] == prop).cumprod(dim=1).sum(dim=1)
        pend = pred[ar, acc]
        keep = off <= acc.view(-1, 1)
        valid[:, W:W + gamma + 1] = keep            # rejected slots stop being attended
        dvalid[:, W:W + gamma] = keep[:, :gamma]
        dvalid[:, W + gamma] = False                # the draft has not written it yet
        fully = acc == gamma
        last = prop[:, gamma - 1]

        acc_tot += acc                              # kept on device; a per-iteration
        total += B                                  # .tolist() would sync the timed loop
        L, W = L + acc + 1, W + gamma + 1
        if record:
            both = torch.cat([prop, pend.view(B, 1)], dim=1).tolist()
            al = acc.tolist()
            for i in range(B):
                toks[i].extend(both[i][:al[i]] + [both[i][gamma]])
    torch.cuda.synchronize()
    sec = time.time() - t0

    per_tenant = None
    if kind != "no_spec":
        at = acc_tot.tolist()
        total += sum(at)
        per_tenant = []
        for k in range(K):
            rr = [i for i in range(B) if adapter[i].item() == k]
            den = len(rr) * iters * gamma
            per_tenant.append(sum(at[i] for i in rr) / den if den else 0.0)
    return dict(sec=sec, iters=iters, tokens=total, tok_s=total / sec,
                per_tenant_accept=per_tenant,
                emitted=[t[:record] for t in toks] if record else None)
